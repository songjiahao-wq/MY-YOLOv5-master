            self.inplanes = widths[2]
            if layers[3] > 2:
                self.layer4 = Stage(block, self.inplanes, widths[3], group_width, layers[3], stride=1,
                                    dilate=replace_stride_with_dilation[3], cheap_ratio=0.5)
